    <div class="col-xs-12 blog-body">
        <p>
            Today the Bitcoin Mini Team is announcing the release of the Bitcoin Mini v 1.0, a plug-and-play bitcoin full node.
            Their aim with the Mini is to provide a simple solution enabling customers to manage,
            interact with, and support the decentralization of bitcoin.
            It boasts a full stack of open-source software, one step setup, and webpage style interface.</p>
        <p>
            Six months ago, two friends seasoned in bitcoin
            set out to build a no-hassle solution to boost the decentralization of bitcoin.
            When they started the Mini project, there were no simple, affordable, standalone full nodes for consumers on
            the market. Their key insight was seeing the need for a “set it and forget it”
			way to strengthen the network and cast a vote for decentralization. They bootstrapped the project,
            creating a full stack of software including a growing personal bitcoin API running
            on the built-in server. The team has plans to offer similar products for most
            major cryptocurrencies. They are proud of the comparibility of
            this product to other comparable projects that have raised over $100 million.</p>
        <p>
            The Mini enters the market providing beginners or power users just the
            right combination of hardware and software. The team is offering a
            pre-order discounted price of $149. Mining bitcoin isn't the only thing
            that nodes can do. Simply running a full node in your home strengthens the
            network by validating transactions and securing the network. That means your
            bitcoin investment is more secure. The Mini costs only pennies a day to run, plug-and-play,
            and gives you peace of mind that the network is healthy.</p>
