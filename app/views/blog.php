        <p>
            Thanks to all the people who ordered the Mini. We are happy to say that we are shipping a day early! We'll 
            be sending out email notices with a tracking number for each order soon. Expect to get your Minis in a few days.</p>
        <p>
            We’ve been working very long hours getting everything set. Many early adopters of bitcoin have low 
            expectations of bitcoin companies’ ability to deliver product on time. We couldn’t let ourselves 
            miss the promised shipping date. We are a very small operation, funding the project ourselves at 
            this point, so you can imagine the late nights we’ve put in. Our first US customers are getting their 
            Minis early, and we hope to open up to international customers in the next couple of weeks.</p>
        <p>
            We had to choose our hardware and software pretty carefully. For example, we use the compact Arch 
            distro of Linux, the compact Arch Linux and Node.js were chosen for their lightweight, wide use, 
            and security. We are open source from head to toe, and improvements in the future will follow 
            that same ethos. It’s important to us to encourage our customers to examine their Mini’s and 
            build their own programs for it. The Mini is not just a [bitcoin] full node, we want to open a 
            new ecosystem, one which sits on a bitcoin full node in the home. We hope to add more decentralization 
            to the network, while providing an avenue for new apps.</p>
        <p>
            In the last two weeks we were able to shrink our code down to offer a more streamlined structure 
            for others to build applications for the Mini. That process is still evolving, but the promise of 
            an ecosystem of apps built just for a personal bitcoin full node, beyond a Bitcoin Core wallet, is 
            very exciting to us, and we are trying our best to make it easy. We’ve already spoken to several 
            developers about bundling their software on the Mini with positive responses.</p>
